    /**
     * Criação do estado da janela CriarConfGUI
     * @param f
     * @param con 
     */
    public CriarConfGUI(ConfiguraFacil f,Configuracao con) {
        this.d = f;
        this.auxC = con;
        this.tipos = new ArrayList<>();
        con.getComponentes().forEach((a)->{
            this.d.getComponentesEssenciais().stream().filter((p)->(p.getID().equals(a) && !this.tipos.contains(p.getTipo())))
                .forEachOrdered((p)->{
                    this.tipos.add(p.getTipo());
            });
         });
        this.model1 = new DefaultListModel();
        this.model2 = new DefaultListModel();
        this.d.getComponentesEssenciais().forEach((a)->{
            if(!this.auxC.getComponentes().contains(a.getID()))
                this.model1.addElement(a.getNome());
        });
        this.auxC.getComponentes().forEach((a)->{
            this.d.getComponentesEssenciais().stream().filter((p) -> (p.getID().equals(a))).forEachOrdered((p) -> {
                this.model2.addElement(p.getNome());
            });
         });
        initComponents();
    }

