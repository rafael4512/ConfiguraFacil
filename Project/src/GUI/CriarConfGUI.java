    /**
     * Escolhe um componente para adicionar
     * @param evt 
     */
    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        Componente aux = this.d.getComponenteNome(this.jList1.getSelectedValue());
        if(aux!=null) {
            this.jLabel8.setText(aux.getDescricao());
            if (evt.getClickCount()== 2 && this.model2.getSize()<=5){
                if(!this.tipos.contains(aux.getTipo())) {
                    this.auxC.addComponente(aux.getID(),aux.getPreco());
                    dispose();
                    execCriarConfGUI();
                }
                else {
                    Object[] options = {"SIM","NÃO"};
                    int result = JOptionPane.showOptionDialog(null, "Já existe um Componente do tipo "+aux.getTipo()+".\nDeseja troca-lo?"
                        ,"TIPOS IGUAIS",JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE,null,options,null );
                    if (result == JOptionPane.YES_OPTION) {
                        this.auxC = this.d.remComp(this.auxC,aux.getTipo());
                        List<String> add = new ArrayList<>();
                        add.add(aux.getID());
                        this.auxC = this.d.addComponente(this.auxC,add);
                        dispose();
                        execCriarConfGUI();
                    }
                }
                
            }
        }
    }//GEN-LAST:event_jList1MouseClicked
    
